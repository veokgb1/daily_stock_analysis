def get_code_by_name(name: str) -> Optional[str]:
    """
    按名称（精确或去空格）查 6 位代码。
    返回 None 表示不在词典中；返回空串表示词典收录但未上市。
    """
    name = name.strip()
    code = STOCK_NAME_MAP.get(name)
    if code is not None:
        return code
    # 去空格再试
    name_ns = name.replace(" ", "").replace("\u3000", "")
    return STOCK_NAME_MAP.get(name_ns)


def enrich_items(items: list) -> list:
    """
    用本地词典补充 StockItem 中缺失或 valid=False 的 code。
    修改后返回新列表，不改变入参。
    """
    result = []
    for item in items:
        it = dict(item)
        if not it.get("valid", True) or not re.match(r"^\d{6}$", it.get("code", "")):
            local = get_code_by_name(it.get("name", ""))
            if local and re.match(r"^\d{6}$", local):
                it["code"]  = local
                it["valid"] = True
        result.append(it)
    return result


def refresh_from_akshare() -> int:
    """
    尝试从 akshare 拉取最新股票列表并补充到 STOCK_NAME_MAP。
    失败静默，不影响启动。返回新增条目数。
    """
    added = 0
    try:
        import akshare as ak  # type: ignore
        df = ak.stock_info_a_code_name()
        # 列名通常为 code / name
        code_col = "code" if "code" in df.columns else df.columns[0]
        name_col = "name" if "name" in df.columns else df.columns[1]
        for _, row in df.iterrows():
            code = str(row[code_col]).strip().zfill(6)
            name = str(row[name_col]).strip()
            if code and name and name not in STOCK_NAME_MAP:
                STOCK_NAME_MAP[name] = code
                added += 1
        logger.info(f"[StockMapping] akshare 刷新完成，新增 {added} 条")
    except Exception as exc:
        logger.debug(f"[StockMapping] akshare 刷新失败（可忽略）: {exc}")
    return added
